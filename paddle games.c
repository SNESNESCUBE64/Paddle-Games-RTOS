void updateBall()
{
    int newXPos = getNewXPos();
    int newYPos = getNewYPos();
    int8_t collision = checkCollision(newXPos, newYPos);//checks and handles the collision with walls
    if(collision != NO_COLLISION)//there was a collision
    {
      if(collision != PLAYER_DIED)//the player did not die
      {
        ballDirection = changeDirection(collision);//change the direction based on the collision
        updateBall();//the direction has changed, lets try again...
      }
      else
        died = true;//set the flag to handle the death
    }
    else//no collision, move normally
    {
        drawCircle(ballPosX, ballPosY, BALL_RADIUS, true, ClrBlack);//erase the ball
        ballPosX = newXPos;//update positions
        ballPosY = newYPos;
        drawCircle(ballPosX, ballPosY, BALL_RADIUS, true, ClrWhite);//draw the ball
    }
}

int getNewXPos()
{
    if((ballDirection == DOWN_RIGHT) || (ballDirection == UP_RIGHT))
        return ballPosX + 1;//move right
    else
        return ballPosX - 1;//move left
}

int getNewYPos()
{
    if((ballDirection == DOWN_RIGHT) || (ballDirection == DOWN_LEFT))
        return ballPosY + 1;//move down
    else
        return ballPosY - 1;//move up
}

int checkCollision(int newPosX, int newPosY)
{
    if(newPosX <= 4)//hit left limit (check for paddle)
    {
        int i = 0;//the temporary counter...
        /*for(i = 0; i < 4; i++)//check to see if any part of the paddle hits the ball
        {
            if((ballPosY + i) >= (leftPaddlePos-1) && (ballPosY+i) < (leftPaddlePos + PADDLE_SIZE))
                return 0;//return if it detected it
        }*/

        for(i = 0; i < PADDLE_SIZE; i++)
        {
            if(abs((i + leftPaddlePos) - ballPosY) <= 3)//basically if it is anywhere near the paddle
                return 0;
        }

        return -2;//the player died, lose a life
    }
    else if(newPosX == BOARD_RIGHT-2)//hit right wall
        return 1;
    else if(newPosY == BOARD_TOP+3)//hit top wall
        return 2;
    else if(newPosY == BOARD_BOTTOM-1)//hit bottom wall
        return 3;
    else if(newPosX > 153)//account for the block setup
        return checkForBlock();
    else
        return -1;//no collision
}

int changeDirection(int collision)
{
    if(ballDirection == DOWN_RIGHT && collision == BOTTOM_WALL_COLLISION)
        return UP_RIGHT;
    else if(ballDirection == DOWN_LEFT && collision == BOTTOM_WALL_COLLISION)
        return UP_LEFT;
    else if(ballDirection == UP_RIGHT && collision == TOP_WALL_COLLISION)
        return DOWN_RIGHT;
    else if(ballDirection == UP_LEFT && collision == TOP_WALL_COLLISION)
        return DOWN_LEFT;
    else if(ballDirection == UP_RIGHT && collision == RIGHT_WALL_COLLISION)
        return UP_LEFT;
    else if(ballDirection == DOWN_RIGHT && collision == RIGHT_WALL_COLLISION)
        return DOWN_LEFT;
    else if(ballDirection == UP_LEFT && collision == PADDLE_COLLISION)
        return UP_RIGHT;
    else if(ballDirection == DOWN_LEFT && collision == PADDLE_COLLISION)
        return DOWN_RIGHT;
    else if(ballDirection == DOWN_RIGHT && collision == BLOCK_COLLISION)
        return DOWN_LEFT;
    else if(ballDirection == UP_RIGHT && collision == BLOCK_COLLISION)
        return UP_LEFT;
    else if(ballDirection == DOWN_LEFT && collision == BLOCK_COLLISION)
        return DOWN_RIGHT;
    else if(ballDirection == UP_LEFT && collision == BLOCK_COLLISION)
        return UP_RIGHT;
    else
        return -1;//lock up the game, but should never happen
}

uint8_t readPot()
{
    uint32_t ui32ADC0Value[2] = {0};//The values of the Potentiometers

    ADCIntClear(ADC0_BASE, 1);//clear the interrupt
    ADCProcessorTrigger(ADC0_BASE, 1);

    while(!ADCIntStatus(ADC0_BASE, 1, false))//wait for the data to be ready
    {
    }

    ADCSequenceDataGet(ADC0_BASE, 1, ui32ADC0Value);//read the data

    if(game == -1)
    {
        if(ui32ADC0Value[1] > 1090)
            return 1;//for breakout
        else
            return 0;//for pong
    }

    uint8_t paddlePosLeft = calculatePaddlePos(true, ui32ADC0Value[1]);
    uint8_t paddlePosRight = calculatePaddlePos(false, ui32ADC0Value[0]);

    if(abs(paddlePosLeft - leftPaddlePos) > 1)
        updatePaddle(paddlePosLeft);

    return 0;
}

