int8_t checkForBlock()//returns based on whether or not it hit a block
{
    //returns...
    //-1 - no hit
    // 0 - left hit
    // 1 - right hit
    // 2 - top hit
    // 3 - bottom hit
    //the old way of doing it, this should never be called...

    int ballTop = ballPosY - 3;
    int ballBottom = ballPosY + 3;
    int ballLeft = ballPosX - 3;
    int ballRight = ballPosX + 3;

    //check top
    int arrayY = ((ballTop - BOARD_TOP + 6)/13)-1;//find out the array locations based on pixel locations
    int arrayX = (ballPosX - (BOARD_RIGHT - 160))/10;

    if(blockGrid[arrayX][arrayY] && ballPosX > 156 && arrayY >= 0)//if there is a block there...
    {
        blockGrid[arrayX][arrayY] = 0;
        removeBlock(arrayX,arrayY);
        score += 5;
        numberOfBlocks--;
        updateScoreBoard();
        return 2;//it hit a block
    }

    //check bottom
    arrayY = ((ballBottom - BOARD_TOP + 6)/13)-1;//find out the array locations based on pixel locations
    arrayX = (ballPosX - (BOARD_RIGHT - 160))/10;

    if(blockGrid[arrayX][arrayY] && ballPosX > 156 && arrayY < 16)//if there is a block there...
    {
        blockGrid[arrayX][arrayY] = 0;
        removeBlock(arrayX,arrayY);
        score += 5;
        numberOfBlocks--;
        updateScoreBoard();
        return 3;//it hit a block
    }

    arrayY = ((ballPosY - BOARD_TOP + 6)/13)-1;//find out the array locations based on pixel locations
    arrayX = (ballRight - (BOARD_RIGHT - 160))/10;

    //check right
    if(blockGrid[arrayX][arrayY] && ballPosY > (BOARD_TOP + 6) && arrayX < 16)//if there is a block there...
    {
        blockGrid[arrayX][arrayY] = 0;
        removeBlock(arrayX,arrayY);
        score += 5;
        numberOfBlocks--;
        updateScoreBoard();
        return 1;//it hit a block
    }

    //check left
    arrayY = ((ballPosY - BOARD_TOP + 6)/13)-1;//find out the array locations based on pixel locations
    arrayX = (ballLeft - (BOARD_RIGHT - 160))/10;

    if(blockGrid[arrayX][arrayY] && ballPosY > (BOARD_TOP + 6) && arrayX >= 0)//if there is a block there...
    {
        blockGrid[arrayX][arrayY] = 0;
        removeBlock(arrayX,arrayY);
        score += 5;
        numberOfBlocks--;
        updateScoreBoard();
        return 0;//it hit a block
    }
    return -1;
}

